    /*Devuelve el numero de indentificacion, si este es vacion retorna un -1, si no es vacia obtiene correctamente el
    dato*/
    public int getIdentificationCard(){
        try{
            if(txtIdentificationCard.getText().equals("")){
                return -1;
            
            }else{
               return Integer.parseInt(txtIdentificationCard.getText());
            }
            
        }catch(NumberFormatException e){
             return -1;
        }
    }
    
    //Obtiene el usuario
    public User getUser(){
        return new User(getIdentificationCard(),PSPassword.getText());
    }
    
